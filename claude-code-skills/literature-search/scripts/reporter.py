"""Generate reports from search results: JSON, Markdown, BibTeX, and final organized reports."""

import json
import os
import logging
from collections import OrderedDict
from datetime import datetime
from typing import List, Dict, Any, Optional

from sources.base import PaperItem

logger = logging.getLogger("lit-search.reporter")


def generate_report(
    items: List[PaperItem],
    output_dir: str,
    timestamp: str,
    output_format: str = "all",
    keywords: List[str] = None,
) -> Dict[str, str]:
    """Generate output files. Returns dict of format -> file path."""
    output_files = {}
    keywords = keywords or []

    if output_format in ("json", "all"):
        path = os.path.join(output_dir, f"{timestamp}_papers.json")
        _write_json(items, path, keywords)
        output_files["json"] = path

    if output_format in ("markdown", "all"):
        path = os.path.join(output_dir, f"{timestamp}_papers.md")
        _write_markdown(items, path, keywords)
        output_files["markdown"] = path

    if output_format in ("bibtex", "all"):
        path = os.path.join(output_dir, f"{timestamp}_papers.bib")
        _write_bibtex(items, path)
        output_files["bibtex"] = path

    return output_files


def _write_json(items: List[PaperItem], path: str, keywords: List[str]):
    data = {
        "meta": {
            "generated_at": datetime.now().isoformat(),
            "total_items": len(items),
            "keywords": keywords,
        },
        "papers": [item.to_dict() for item in items],
    }
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    logger.info(f"JSON report: {path}")


def _write_bibtex(items: List[PaperItem], path: str):
    """Write BibTeX file with all papers."""
    # Track used cite keys to avoid duplicates
    used_keys = set()
    entries = []

    for item in items:
        bib = item.to_bibtex()
        # Extract and deduplicate cite key
        key_start = bib.index("{") + 1
        key_end = bib.index(",")
        cite_key = bib[key_start:key_end]

        if cite_key in used_keys:
            # Append suffix
            suffix = "b"
            while f"{cite_key}{suffix}" in used_keys:
                suffix = chr(ord(suffix) + 1)
            new_key = f"{cite_key}{suffix}"
            bib = bib[:key_start] + new_key + bib[key_end:]
            cite_key = new_key

        used_keys.add(cite_key)
        entries.append(bib)

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n\n".join(entries))
        f.write("\n")
    logger.info(f"BibTeX file: {path} ({len(entries)} entries)")


def _write_markdown(items: List[PaperItem], path: str, keywords: List[str]):
    today = datetime.now().strftime("%Y-%m-%d %H:%M")
    lines = ["# Literature Search Results\n"]
    lines.append(f"**Date**: {today}  ")
    lines.append(f"**Keywords**: {', '.join(keywords)}  ")

    source_counts: Dict[str, int] = {}
    for item in items:
        source_counts[item.source_type] = source_counts.get(item.source_type, 0) + 1
    source_str = ", ".join(f"`{s}`({c})" for s, c in sorted(source_counts.items()))
    lines.append(f"**Sources**: {source_str}  ")
    lines.append(f"**Total**: {len(items)} papers  \n")
    lines.append("---\n")

    # Group by search round if available, otherwise flat list
    rounds: Dict[str, List[PaperItem]] = {}
    for item in items:
        key = item.search_round or "all"
        rounds.setdefault(key, []).append(item)

    for round_id, round_items in rounds.items():
        if round_id != "all":
            lines.append(f"\n## Round: {round_id}\n")
        else:
            lines.append(f"\n## Papers\n")

        for i, item in enumerate(round_items, 1):
            lines.append(_format_paper(item, i))

    lines.append("\n---\n")
    lines.append(f"*Generated by literature-search at {today}*\n")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    logger.info(f"Markdown report: {path}")


def _format_paper(item: PaperItem, index: int) -> str:
    parts = [f"### {index}. [{item.title}]({item.url})\n"]

    meta = []
    if item.authors:
        authors_str = ", ".join(item.authors[:5])
        if len(item.authors) > 5:
            authors_str += f" et al. ({len(item.authors)})"
        meta.append(f"**Authors**: {authors_str}")
    if item.year:
        meta.append(f"**Year**: {item.year}")
    meta.append(f"**Source**: `{item.source_type}`")
    if item.venue:
        meta.append(f"**Venue**: {item.venue}")
    if item.citation_count > 0:
        meta.append(f"**Citations**: {item.citation_count}")
    if item.doi:
        meta.append(f"**DOI**: {item.doi}")

    parts.append(" | ".join(meta) + "  \n")

    if item.abstract:
        parts.append(f"> {item.abstract}\n")

    if item.tldr:
        parts.append(f"**TL;DR**: {item.tldr}  \n")

    score_stars = "★" * int(item.relevance_score * 5) + "☆" * (5 - int(item.relevance_score * 5))
    score_line = f"Relevance: {score_stars} ({item.relevance_score:.2f})"
    if item.matched_keywords:
        score_line += f" | Matched: {', '.join(item.matched_keywords)}"
    if item.content_type:
        score_line += f" | Type: {item.content_type}"
    parts.append(f"*{score_line}*\n")

    return "\n".join(parts)


def generate_final_report(
    categories: Dict[str, List[PaperItem]],
    output_dir: str,
    timestamp: str,
    topic_description: str = "",
) -> Dict[str, str]:
    """Generate the final organized Related Work report as multiple files.

    Output structure:
        {timestamp}_00_overview.md          — summary tables with links to sub-direction files
        {timestamp}_01_{safe_name}.md       — full details for sub-direction 1
        {timestamp}_02_{safe_name}.md       — full details for sub-direction 2
        ...
        {timestamp}_final_report.bib        — all BibTeX entries
        {timestamp}_final_report.json       — structured JSON

    Args:
        categories: OrderedDict of {category_name: [PaperItem, ...]}.
        output_dir: Output directory.
        timestamp: Timestamp string for filenames.
        topic_description: Brief description of the research topic.

    Returns:
        Dict of format -> file path (or list of paths for markdown_categories).
    """
    os.makedirs(output_dir, exist_ok=True)
    output_files = {}

    # 1. Build category file mapping (name -> filename)
    cat_file_map = OrderedDict()
    for idx, cat_name in enumerate(categories.keys(), 1):
        safe_name = _sanitize_filename(cat_name)
        filename = f"{timestamp}_{idx:02d}_{safe_name}.md"
        cat_file_map[cat_name] = filename

    # 2. Overview MD (summary tables + links to sub-direction files)
    overview_path = os.path.join(output_dir, f"{timestamp}_00_overview.md")
    _write_overview_markdown(categories, cat_file_map, overview_path, topic_description)
    output_files["markdown"] = overview_path

    # 3. One MD per sub-direction
    cat_paths = []
    global_idx = 0
    for cat_name, items in categories.items():
        cat_filename = cat_file_map[cat_name]
        cat_path = os.path.join(output_dir, cat_filename)
        _write_category_markdown(cat_name, items, cat_path, global_idx, topic_description)
        cat_paths.append(cat_path)
        global_idx += len(items)
    output_files["markdown_categories"] = cat_paths

    # 4. Final BibTeX (only selected papers)
    all_items = []
    for items in categories.values():
        all_items.extend(items)
    bib_path = os.path.join(output_dir, f"{timestamp}_final_report.bib")
    _write_bibtex(all_items, bib_path)
    output_files["bibtex"] = bib_path

    # 5. Final JSON
    json_path = os.path.join(output_dir, f"{timestamp}_final_report.json")
    _write_final_json(categories, json_path, topic_description)
    output_files["json"] = json_path

    return output_files


def _sanitize_filename(name: str) -> str:
    """Convert a category name like 'A. AIGC/LLM Inference' to a safe filename slug."""
    import re
    # Remove leading letter+dot prefix (e.g. "A. " or "B. ")
    slug = re.sub(r"^[A-Z]\.\s*", "", name)
    # Replace non-alphanumeric chars with underscore
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", slug)
    # Collapse multiple underscores and strip
    slug = re.sub(r"_+", "_", slug).strip("_")
    return slug[:60] or "category"


def _write_overview_markdown(
    categories: Dict[str, List[PaperItem]],
    cat_file_map: Dict[str, str],
    path: str,
    topic_description: str,
):
    """Write the overview file with summary tables and links to sub-direction files."""
    today = datetime.now().strftime("%Y-%m-%d %H:%M")
    total = sum(len(items) for items in categories.values())

    lines = ["# Related Work Literature Survey Report\n"]
    lines.append(f"**Date**: {today}  ")
    if topic_description:
        lines.append(f"**Topic**: {topic_description}  ")
    lines.append(f"**Total Selected**: {total} papers  ")
    lines.append(f"**Sub-directions**: {len(categories)}  \n")
    lines.append("---\n")

    # === Sub-direction index ===
    lines.append("\n## Sub-direction Files\n")
    for cat_name, filename in cat_file_map.items():
        count = len(categories[cat_name])
        lines.append(f"- [{cat_name}]({filename}) ({count} papers)")
    lines.append("")

    lines.append("---\n")

    # === Summary Recommendation Tables ===
    lines.append("\n## Overview: Recommended Papers by Sub-direction\n")

    for cat_name, items in categories.items():
        filename = cat_file_map[cat_name]
        lines.append(f"\n### [{cat_name}]({filename})\n")
        lines.append("| # | Paper | Venue | Year | Citations | Core Contribution |")
        lines.append("|---|-------|-------|------|-----------|-------------------|")

        for i, item in enumerate(items, 1):
            title_short = item.title
            if len(title_short) > 70:
                title_short = title_short[:67] + "..."
            cite_str = str(item.citation_count) if item.citation_count > 0 else "-"
            venue_short = item.venue
            if len(venue_short) > 35:
                venue_short = venue_short[:32] + "..."
            contrib = ""
            if item.tldr:
                contrib = item.tldr
            elif item.abstract:
                first_sent = item.abstract.split(". ")[0]
                if len(first_sent) > 120:
                    first_sent = first_sent[:117] + "..."
                contrib = first_sent
            lines.append(
                f"| {i} | [{title_short}]({item.url}) | {venue_short} | {item.year or '-'} | {cite_str} | {contrib} |"
            )

        lines.append("")

    # === Footer ===
    lines.append("\n---\n")
    lines.append(f"*Generated by literature-search at {today}*\n")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    logger.info(f"Overview report: {path} ({total} papers, {len(categories)} sub-directions)")


def _write_category_markdown(
    cat_name: str,
    items: List[PaperItem],
    path: str,
    global_start_idx: int,
    topic_description: str,
):
    """Write a single sub-direction file with full paper details."""
    today = datetime.now().strftime("%Y-%m-%d %H:%M")

    lines = [f"# {cat_name}\n"]
    if topic_description:
        lines.append(f"**Topic**: {topic_description}  ")
    lines.append(f"**Papers in this sub-direction**: {len(items)}  ")
    lines.append(f"[← Back to Overview](.)  \n")
    lines.append("---\n")

    for i, item in enumerate(items, 1):
        idx = global_start_idx + i
        lines.append(_format_paper_full(item, idx))

    # === Footer ===
    lines.append("\n---\n")
    lines.append(f"*Generated by literature-search at {today}*\n")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    logger.info(f"Category report: {path} ({len(items)} papers)")


def _format_paper_full(item: PaperItem, index: int) -> str:
    """Format a single paper with full details (no truncation)."""
    parts = [f"#### {index}. [{item.title}]({item.url})\n"]

    # Metadata line
    meta = []
    if item.authors:
        authors_str = ", ".join(item.authors)
        meta.append(f"**Authors**: {authors_str}")
    if item.year:
        meta.append(f"**Year**: {item.year}")
    if item.venue:
        meta.append(f"**Venue**: {item.venue}")
    if item.citation_count > 0:
        meta.append(f"**Citations**: {item.citation_count}")
    if item.doi:
        meta.append(f"**DOI**: {item.doi}")
    if item.content_type:
        meta.append(f"**Type**: {item.content_type}")

    parts.append("  \n".join(meta) + "  \n")

    # Full abstract (never truncated)
    if item.abstract:
        parts.append(f"\n**Abstract**: {item.abstract}\n")

    if item.tldr:
        parts.append(f"\n**TL;DR**: {item.tldr}\n")

    # BibTeX
    parts.append(f"\n<details><summary>BibTeX</summary>\n")
    parts.append(f"\n```bibtex\n{item.to_bibtex()}\n```\n")
    parts.append(f"\n</details>\n")

    return "\n".join(parts)


def _write_final_json(
    categories: Dict[str, List[PaperItem]],
    path: str,
    topic_description: str,
):
    """Write final organized report as JSON."""
    data = {
        "meta": {
            "generated_at": datetime.now().isoformat(),
            "topic": topic_description,
            "total_papers": sum(len(v) for v in categories.values()),
            "categories": list(categories.keys()),
        },
        "categories": {},
    }
    for cat_name, items in categories.items():
        data["categories"][cat_name] = [item.to_dict() for item in items]

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    logger.info(f"Final JSON: {path}")
